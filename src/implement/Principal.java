    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (count == fundo.length - 1) {
            count = 0;
        } else {
            count++;
        }
        Panel p = new Panel();
        jScrollPane1.setViewportView(p);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (count > 0) {
            count--;
        } else {
            count = fundo.length - 1;
        }

        Panel p = new Panel();
        jScrollPane1.setViewportView(p);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if (rmi.deletarFoto(
                    JOptionPane.showConfirmDialog(this,
                            "Deseja excluir a imagem " + rmi.obterFiles()[count].getName()),
                    rmi.obterFiles()[count])) {

                JOptionPane.showMessageDialog(this, "Excluído com sucesso");
                this.count = 0;
                this.fundo = rmi.carregarFotosSemConf();
                Panel p = new Panel();
                jScrollPane1.setViewportView(p);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir",
                        "Excluir", JOptionPane.ERROR_MESSAGE);
            }
        } catch (RemoteException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

