    public void abrirRMI() {
        try {
            registro = LocateRegistry.getRegistry("127.0.0.1", 2020);
            rmi = (InterfaceServidorCliente) registro.lookup("servidor");

            this.fundo = rmi.carregarFotos();
            this.jTextField1.setText(rmi.obterPath());
        } catch (RemoteException ex) {
            ex.printStackTrace();
        } catch (NotBoundException ex) {
            ex.printStackTrace();
        }
    }

